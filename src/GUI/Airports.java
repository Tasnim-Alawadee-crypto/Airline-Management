    public void viewAirports() {
        try {
            // 1. مسح الصفوف القديمة من الجدول
            // هذا يضمن أن البيانات لا تتراكم في كل مرة نضغط فيها على زر View
            tableModel.setRowCount(0);

            // 2. جلب قائمة المطارات من قاعدة البيانات
            // نفترض أن لديك دالة اسمها getAllAirports() في كلاس الـ DAO
            List<Airport> airports = dao.getAllAirports();

            // 3. إضافة كل مطار إلى الجدول
            for (Airport airport : airports) {
                // إضافة البيانات كصف جديد في الـ tableModel
                tableModel.addRow(new Object[]{

                        airport.getName(),      // يجب أن يتطابق مع 'Title 1'
                        airport.getLocation(),  // يجب أن يتطابق مع 'Title 2'
                        airport.getCode(),// يجب أن يتطابق مع 'Title 3'
                });
            }

        } catch (Exception ex) {
            // في حال حدوث خطأ أثناء الاتصال بقاعدة البيانات
            JOptionPane.showMessageDialog(null, "حدث خطأ أثناء تحميل البيانات: " + ex.getMessage(), "خطأ", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

