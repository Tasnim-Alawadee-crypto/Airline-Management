    private void updateAirport() {
        int selectedRow = table.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null,"Please select an airport to update.","Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        try {
            // قراءة البيانات الحالية من الجدول
            String currentCode = tableModel.getValueAt(selectedRow, 0).toString(); // نفترض أن العمود 0 هو Code
            String currentName = tableModel.getValueAt(selectedRow, 1).toString();
            String currentLocation = tableModel.getValueAt(selectedRow, 2).toString();

            // طلب البيانات الجديدة من المستخدم
            String name = JOptionPane.showInputDialog(null,"Enter new airport name:",currentName);
            if (name == null) return; // المستخدم ألغى
            String location = JOptionPane.showInputDialog(null,"Enter new location:",currentLocation);
            if (location == null) return;

            // تحقق من أن البيانات ليست فارغة
            name = name.trim();
            location = location.trim();
            if (name.isEmpty() || location.isEmpty() ) {
                JOptionPane.showMessageDialog(null,"Name and location cannot be empty!","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }

            // إنشاء كائن Airport جديد للتحديث
            Airport airport = new Airport(name,location,currentCode);

            // استدعاء DAO لتحديث قاعدة البيانات
            dao.updateAirport(airport);

            JOptionPane.showMessageDialog(null,"Airport updated successfully!");

            // إعادة تحميل البيانات في الجدول
            viewAirports(); // دالة تقوم بملء الجدول من قاعدة البيانات
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Error updating airport: " + ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }

