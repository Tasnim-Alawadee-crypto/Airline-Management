    private void addAirport() {
        try {
            // قراءة البيانات من حقول النصوص
            String name = txtName.getText().trim();
            String location = txtLocation.getText().trim();
            String code = txtCode.getText().trim();

            // التحقق من أن الحقول ليست فارغة
            if (name.isEmpty() || location.isEmpty() || code.isEmpty()) {
                JOptionPane.showMessageDialog(null,"Please fill all fields!","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }

            // إنشاء كائن Airport
            Airport airport = new Airport(name,location,code);

            // إنشاء كائن DAO لاستدعاء الدالة
            DAO dao = new DAO();
            dao.addAirport(airport);

            // رسالة نجاح
            JOptionPane.showMessageDialog(null,"Airport added successfully!");

            // تنظيف الحقول
            clearFields();

            // تحديث الجدول في الواجهة
            viewAirports();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Error while adding airport!\n" + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }



