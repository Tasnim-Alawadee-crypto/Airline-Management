  private void deleteFlight() {
    int selectedRow = table.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Please select a flight to delete from the table.", "Info", JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    try {
        String flightNumber = tableModel.getValueAt(selectedRow, 0).toString(); // العمود الأول هو flightNumber
        DAO dao = new DAO();
        dao.deleteFlight(flightNumber);

        JOptionPane.showMessageDialog(null, "Flight deleted successfully!");
        viewFlights(); // تحديث الجدول بعد الحذف
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error deleting flight:\n" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
}

 
    
    private void viewFlights() {
    DAO dao = new DAO();
    List<Flight> flights = dao.getAllFlights();
    tableModel.setRowCount(0); // مسح البيانات القديمة من الجدول

    for (Flight flight : flights) {
        tableModel.addRow(new Object[]{
            flight.getFlightNumber(),
            flight.getOrigin(),
            flight.getDestination(),
            flight.getDepartureTime(),
            flight.getArrivalTime(),
            flight.getStatus()
        });
    }
}

    
    
    private void addFlight() {
    try {
        // قراءة البيانات من حقول النصوص
        String flightNumber = txtFlightNumber.getText().trim();
        String origin = txtOrigin.getText().trim();
        String destination = txtDestination.getText().trim();
        String departure = txtDeparture.getText().trim();
        String arrival = txtArrival.getText().trim();

        // التحقق من أن الحقول ليست فارغة
        if (flightNumber.isEmpty() || origin.isEmpty() || destination.isEmpty() || departure.isEmpty() || arrival.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all fields!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // إنشاء كائن Flight
        Flight flight = new Flight(flightNumber, origin, destination, departure, arrival);

        // استدعاء DAO لإضافة الرحلة
        DAO dao = new DAO();
        dao.addFlight(flight);

        // رسالة نجاح
        JOptionPane.showMessageDialog(null, "Flight added successfully!");

        // تنظيف الحقول
        clearFields();

        // تحديث الجدول بعد الإضافة
        viewFlights();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error while adding flight!\n" + ex.getMessage(),
                                      "Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
}

    
