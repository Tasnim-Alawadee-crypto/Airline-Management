private void updateFlight() {
    int selectedRow = table.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Please select a flight to update from the table.", "Info", JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    try {
        // نفترض أن العمود الأول هو flightNumber
        String flightNumber = tableModel.getValueAt(selectedRow, 0).toString();
        String currentOrigin = tableModel.getValueAt(selectedRow, 1).toString();
        String currentDestination = tableModel.getValueAt(selectedRow, 2).toString();
        String currentDeparture = tableModel.getValueAt(selectedRow, 3).toString();
        String currentArrival = tableModel.getValueAt(selectedRow, 4).toString();

        String origin = JOptionPane.showInputDialog(null, "Enter new origin:", currentOrigin);
        if (origin == null) return; // user cancelled
        String destination = JOptionPane.showInputDialog(null, "Enter new destination:", currentDestination);
        if (destination == null) return;
        String departure = JOptionPane.showInputDialog(null, "Enter new departure time:", currentDeparture);
        if (departure == null) return;
        String arrival = JOptionPane.showInputDialog(null, "Enter new arrival time:", currentArrival);
        if (arrival == null) return;

        origin = origin.trim();
        destination = destination.trim();
        departure = departure.trim();
        arrival = arrival.trim();
        if (origin.isEmpty() || destination.isEmpty() || departure.isEmpty() || arrival.isEmpty()) {
            JOptionPane.showMessageDialog(null, "All fields must be filled!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Flight flight = new Flight(flightNumber, origin, destination, departure, arrival);
        DAO dao = new DAO();
        dao.updateFlight(flight);

        JOptionPane.showMessageDialog(null, "Flight updated successfully!");
        viewFlights(); // تحديث الجدول بعد التعديل
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error updating flight:\n" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
}

